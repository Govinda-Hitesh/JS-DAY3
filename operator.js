//operators in js

//arithmetic operators
//+,-,*,/,%

console.log(2*3);
console.log(2/3);
console.log(3%4);


//comparison operators  
// >,>,<=,>=,==,===

console.log(2<3);
console.log(3>=4);
console.log(2===2);

//logical operators

ons.log(2||3);//3
console.log(0||3);//3
console.log(1&&3);//3
console.log(2&&0);//0

console.log(!0);//true
console.log(!2);//false
console.log(!1);//false

//increament decreament operators
//post increment operators(++)
let a = 1;
a++;
console.log(a);//2
a--;
